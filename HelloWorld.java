package DayFour;

public class HelloWorld {

    // Variable is a container where some data is stored
    // Whenever we create a variable some memory will be given to this container.

    // Method definition means how the method should behave
    static void m1(){
        System.out.println("m1 method");
    }

    public static void main(String[] args) {
        int x = 10; // variable
        System.out.println(x);
        System.out.println("Hello World");
        m1();  // method call
    }
}
